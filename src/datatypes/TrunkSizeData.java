package datatypes;

public class TrunkSizeData {
	private int height;
	private int width;
	private int depth;
	
	public TrunkSizeData(int height, int width, int depth) {
		this.height = height;
		this.width = width;
		this.depth = depth;
	}

	public int getHeight() {
		return height;
	}

	public int getWidth() {
		return width;
	}

	public int getDepth() {
		return depth;
	}
	
}
